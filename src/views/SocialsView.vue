  <script>
  export default {
    name: 'SocialsView',
    components: {
    },
    methods: {
      changeVisibility(name,id) {
        //Toggle Visibility for all Elements
        var elements = document.getElementsByName(name);
        for(let i=0; i < elements.length; i++) {
          if(elements[i].style.visibility == "visible") {
            elements[i].style.visibility = "hidden";
            elements[i].style.display = "none";
          } else {
            elements[i].style.visibility = "visible";
            elements[i].style.display = "block";  
          }
        } 
        //Toggle Month
        if(document.getElementById(id).style.color == "white") {
          document.getElementById(id).style.color = "gray";
          document.getElementById(id).innerHTML = this.SpecialChar(">") + " " + document.getElementById(id).innerHTML.substring(this.SpecialChar("<").length + 1);
        } else {
          document.getElementById(id).style.color = "white";
          document.getElementById(id).innerHTML = this.SpecialChar("<") + " " + document.getElementById(id).innerHTML.substring(this.SpecialChar(">").length + 1);
        }
        //Toggle Tooday
        const date = ("0" + new Date().getDate().toString()).slice(-2);
        const month = ("0" + (new Date().getMonth() + 1).toString()).slice(-2);
        const year = new Date().getFullYear().toString();
        const today = date + month + year;
        document.getElementById(today).style.color = "blue";
        document.getElementById(today).style.fontWeight = "bold";
        document.getElementById(today).style.textDecoration = "underline";
      },
      SpecialChar(text) {
        switch(text) {
          case ">": return "&gt;"
          case "<": return "&lt;"
        }
      }
    }
  }
  </script>
  
